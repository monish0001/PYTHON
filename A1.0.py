#Q6
# In a compiled language, the entire program is translated (compiled) into machine code before execution.
# This machine code is what the computer’s CPU directly understands.
# Fast execution (because code is already compiled).
# Errors are found before running the program
# Needs a compiler (like GCC for C).
# Examples:
# C
# C++
# Java (partly compiled, partly interpreted — uses JVM bytecode)
# Go
# Rust

# In an interpreted language, the program is executed line by line by an interpreter — no need for a separate compilation step.
# Easier to debug (errors appear as soon as the line runs).
# Slower execution (interpreted each time).
# More flexible and platform independent.
# Examples:
# Python
# JavaScript
# Ruby
# PHP
# Bash

